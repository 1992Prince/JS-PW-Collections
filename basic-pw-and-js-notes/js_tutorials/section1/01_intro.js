/*
How can we execute JavaScript code outside of a browser using Node.js?
***************************************************************************

To execute JavaScript code outside of a browser using Node.js:

1) Install Node.js:

Download and install Node.js from the official website:  https://nodejs.org.

2) Write a JavaScript File:

Create a .js file, for example, app.js.
Add JavaScript code to the file:

console.log("Hello, World! Running JavaScript outside the browser!");

3) Run the JavaScript File:

Open your terminal or command prompt.
Navigate to the directory containing the file.
Execute the file using the node command:

node app.js

4) Output:

The terminal will display the output of the JavaScript code:

Hello, World! Running JavaScript outside the browser!
*/

